package Elements;

public class flightClassType {
	int id;
	String classType;
	public flightClassType(int id, String classType) {
		this.id = id;
		this.classType = classType;
	}
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public String getClassType() {
		return classType;
	}
	public void setClassType(String classType) {
		this.classType = classType;
	}
	
}
